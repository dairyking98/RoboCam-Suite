#!/bin/bash
# RoboCam-Suite Preview Application Launcher

set -e  # Exit on error

# Get the directory where this script is located
SCRIPT_DIR="$( cd "$( dirname "${BASH_SOURCE[0]}" )" && pwd )"
cd "$SCRIPT_DIR"

# Player One SDK: full SDK in project root (lib/arm64 or lib/aarch64), or playerone_sdk/native, then /usr/local/lib
for sdk_dir in "PlayerOne_Camera_SDK_Linux_V3.10.0" "PlayerOne_Camera_SDK_Linux_"*; do
  if [ -d "$SCRIPT_DIR/$sdk_dir" ]; then
    for arch in arm64 aarch64 armhf; do
      if [ -d "$SCRIPT_DIR/$sdk_dir/lib/$arch" ]; then
        export LD_LIBRARY_PATH="$SCRIPT_DIR/$sdk_dir/lib/$arch${LD_LIBRARY_PATH:+:$LD_LIBRARY_PATH}"
      fi
    done
    break
  fi
done 2>/dev/null || true
if [ -d "$SCRIPT_DIR/playerone_sdk/native" ]; then
  export LD_LIBRARY_PATH="$SCRIPT_DIR/playerone_sdk/native${LD_LIBRARY_PATH:+:$LD_LIBRARY_PATH}"
fi
export LD_LIBRARY_PATH="/usr/local/lib${LD_LIBRARY_PATH:+:$LD_LIBRARY_PATH}"

# Check if virtual environment exists
if [ ! -d "venv" ]; then
    echo "Error: Virtual environment not found. Please run ./setup.sh first."
    exit 1
fi

# Activate virtual environment
source venv/bin/activate

# Check if preview.py exists
if [ ! -f "preview.py" ]; then
    echo "Error: preview.py not found in current directory."
    exit 1
fi

# Create log directory if it doesn't exist
mkdir -p logs

# Run the preview application
echo "Starting RoboCam-Suite Preview Application..."
echo "Log file: logs/preview_$(date +%Y%m%d_%H%M%S).log"
echo ""

# Pass through all command-line arguments (e.g., --simulate_3d, --simulate_cam, --backend)
# Examples:
#   ./start_preview.sh --simulate_3d
#   ./start_preview.sh --simulate_cam
#   ./start_preview.sh --simulate_3d --simulate_cam
#   ./start_preview.sh --backend qtgl
python preview.py "$@" 2>&1 | tee "logs/preview_$(date +%Y%m%d_%H%M%S).log"

# Deactivate virtual environment on exit
deactivate

